# ==========================================================
# SPATIAL ATTENTION MODULE
# ==========================================================

class SpatialAttention(nn.Module):

    def __init__(self,
                 feature_dim=256):

        super(SpatialAttention, self).__init__()

        self.query = nn.Linear(
            feature_dim,
            feature_dim
        )

        self.key = nn.Linear(
            feature_dim,
            feature_dim
        )

        self.value = nn.Linear(
            feature_dim,
            feature_dim
        )

        self.softmax = nn.Softmax(dim=-1)

        self.scale = feature_dim ** 0.5

    def forward(self, x):

        # x : (N, Feature)

        Q = self.query(x)

        K = self.key(x)

        V = self.value(x)

