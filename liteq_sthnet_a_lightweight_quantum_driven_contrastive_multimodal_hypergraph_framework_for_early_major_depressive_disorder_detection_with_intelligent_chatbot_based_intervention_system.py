# ==========================================================
# NT-XENT LOSS
# ==========================================================

class NTXentLoss(nn.Module):

    def __init__(
        self,
        temperature=0.5
    ):

        super().__init__()

        self.temperature = temperature

    def forward(self, z1, z2):

        batch_size = z1.size(0)

        z1 = F.normalize(z1, dim=1)
        z2 = F.normalize(z2, dim=1)

        representations = torch.cat(
            [z1, z2],
            dim=0
        )

        similarity_matrix = torch.matmul(
            representations,
            representations.T
        )

        mask = torch.eye(
            2 * batch_size,
            dtype=torch.bool
        ).to(similarity_matrix.device)

        similarity_matrix = similarity_matrix[~mask]

        similarity_matrix = similarity_matrix.view(
            2 * batch_size,
            -1
        )

        positives = torch.sum(
            z1 * z2,
            dim=1
        )

        positives = torch.cat(
            [positives, positives],
            dim=0
        )

        logits = torch.cat(
            [
                positives.unsqueeze(1),
                similarity_matrix
            ],
            dim=1
        )

        logits = logits / self.temperature

        labels = torch.zeros(
            logits.shape[0],
            dtype=torch.long
        ).to(logits.device)

        loss = F.cross_entropy(
            logits,
            labels
        )

        return loss

