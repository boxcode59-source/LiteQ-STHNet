import torch
import torch.nn as nn
import torch.nn.functional as F

# ==========================================================
# Lite-ST-HGNN Block
# ==========================================================

class LiteSTHGNNBlock(nn.Module):

    def __init__(self,
                 feature_dim=256,
                 dropout=0.30):

        super(LiteSTHGNNBlock, self).__init__()

        # Hypergraph Convolution
        self.hgconv = HypergraphConv(
            feature_dim,
            feature_dim,
            dropout
        )

        # Spatial Attention
        self.spatial = SpatialAttention(
            feature_dim
        )

        # Temporal Attention
        self.temporal = TemporalAttention(
            feature_dim,
            feature_dim
        )

        # Layer Normalization
        self.norm1 = nn.LayerNorm(feature_dim)

        self.norm2 = nn.LayerNorm(feature_dim)

        # Feed Forward Network
        self.ffn = nn.Sequential(

            nn.Linear(feature_dim,
                      feature_dim * 2),

            nn.ReLU(),

            nn.Dropout(dropout),

            nn.Linear(feature_dim * 2,
                      feature_dim)

        )

        self.dropout = nn.Dropout(dropout)

    def forward(self,
                x,
                G):

        # ---------------------------------------
        # Hypergraph Convolution
        # ---------------------------------------

        h = self.hgconv(
            x,
            G
        )

        # ---------------------------------------
        # Spatial Attention
        # ---------------------------------------

        spatial_out, spatial_weight = self.spatial(
            h
        )

        h = h + spatial_out

        h = self.norm1(h)

        # ---------------------------------------
        # Temporal Attention
        # ---------------------------------------

        temp_input = h.unsqueeze(0)

        temporal_out, temporal_weight = self.temporal(
            temp_input
        )

        temporal_out = temporal_out.squeeze(0)

        h = h + temporal_out

        h = self.norm2(h)

        # ---------------------------------------
        # Feed Forward
        # ---------------------------------------

        ff = self.ffn(h)

        h = h + ff

        h = self.dropout(h)

        return h, spatial_weight, temporal_weight


