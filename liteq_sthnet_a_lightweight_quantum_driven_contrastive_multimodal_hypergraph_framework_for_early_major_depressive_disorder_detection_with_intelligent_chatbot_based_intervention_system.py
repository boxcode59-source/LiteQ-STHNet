# ==========================================================
# HYPERGRAPH CONVOLUTION LAYER
# ==========================================================

class HypergraphConv(nn.Module):

    def __init__(self,
                 in_channels,
                 out_channels,
                 dropout=0.3):

        super(HypergraphConv, self).__init__()

        self.linear = nn.Linear(
            in_channels,
            out_channels,
            bias=True
        )

        self.batch_norm = nn.BatchNorm1d(
            out_channels
        )

        self.dropout = nn.Dropout(
            dropout
        )

    def forward(self,
                x,
                G):

        # Hypergraph propagation
        x = torch.matmul(G, x)

        # Linear transformation
        x = self.linear(x)

        # Batch Normalization
        x = self.batch_norm(x)

        # Activation
        x = F.relu(x)

        # Dropout
        x = self.dropout(x)

        return x

