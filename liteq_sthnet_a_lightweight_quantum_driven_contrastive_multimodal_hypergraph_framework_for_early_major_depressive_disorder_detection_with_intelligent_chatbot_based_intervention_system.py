import torch
import torch.nn as nn
import torch.nn.functional as F

# ==========================================================
# Lite-ST-HGNN
# ==========================================================

class LiteSTHGNN(nn.Module):

    def __init__(self,
                 input_dim=256,
                 hidden_dim=256,
                 num_blocks=3,
                 num_classes=4,
                 dropout=0.30):

        super(LiteSTHGNN, self).__init__()

        self.input_proj = nn.Linear(
            input_dim,
            hidden_dim
        )

        # Stack Lite-ST-HGNN Blocks
        self.blocks = nn.ModuleList()

        for _ in range(num_blocks):

            self.blocks.append(

                LiteSTHGNNBlock(

                    feature_dim=hidden_dim,

                    dropout=dropout

                )

            )

        self.classifier = nn.Sequential(

            nn.Linear(hidden_dim,128),

            nn.ReLU(),

            nn.Dropout(dropout),

            nn.Linear(128,64),

            nn.ReLU(),

            nn.Dropout(dropout),

            nn.Linear(64,num_classes)

        )

    def forward(self,x,G):

        x = self.input_proj(x)

        spatial_maps = []

        temporal_maps = []

        for block in self.blocks:

            x, spatial, temporal = block(x,G)

            spatial_maps.append(spatial)

            temporal_maps.append(temporal)

        # Global Node Pooling
        graph_feature = torch.mean(x,dim=0)

        logits = self.classifier(graph_feature)

        return logits,x,spatial_maps,temporal_maps

